    /**
     * validates Student details from the form Saves the details to a text file if they are valid
     * @param evt
     */
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        boolean valid = true;
        String newID = String.valueOf(StudentDetails.getNewId());
        idTxt.setText(newID);

        valid = validateDetails();

        if (valid == true) {
            StudentDetails newStudent = new StudentDetails(newID, forenameTxt.getText(), surnameTxt.getText(), address1Txt.getText(), address2Txt.getText(), address3Txt.getText(), postCodeTxt.getText(), phoneTxt.getText(), dobTxt.getText());
            String newLine = newStudent.toString() + "\n";

            try {
                FileWriter writer = new FileWriter(StudentDetails.FILE_NAME, true);
                writer.write(newLine);
                writer.flush();
                writer.close();

                JOptionPane.showMessageDialog(null, "Saved Succsfully");//tell the user it worked
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Something went wrong saving Student Details");//tell the user it didn't work
                Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_addBtnActionPerformed

