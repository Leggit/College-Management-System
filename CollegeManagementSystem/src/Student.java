    /**
     * initiates a search for a student with a surname matching the user input
     * @param evt 
     */
    private void goBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goBtnActionPerformed
        String[] details = Search.findRecord(StudentDetails.FILE_NAME, searchTxt.getText(), 2);//method that tries to find a match
        
       
        if(details != null){//if a match was found
            displayData(details);
        }else{//if no match was found
            JOptionPane.showMessageDialog(null, "No match found");//tell the user
            clearForm();//empty the text boxes so there is no confusion
        }
    }//GEN-LAST:event_goBtnActionPerformed

