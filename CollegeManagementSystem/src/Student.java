    /**
     * method that uses a validation class to make sure user entry is sensible and reasonable for each field 
     * it also displays messages that tell the user exactly what was not valid id data is invalid
     * @return 
     */
    public boolean validateDetails(){
        boolean valid = true;
         if (Validation.isPresent(surnameTxt.getText()) == false) {
            valid = false;
            JOptionPane.showMessageDialog(null, "Surname field cannot be empty");
        }
        if (Validation.isDate(dobTxt.getText()) == false) {
            valid = false;
            JOptionPane.showMessageDialog(null, "Date Of Birth must be entered in the form dd/mm/yyyy");
        }
        if (Validation.lengthCheck(phoneTxt.getText(), 11) == false) {
            valid = false;
            JOptionPane.showMessageDialog(null, "Phone Number must be 11 digits long");
        }
        if (Validation.rangeCheck(postCodeTxt.getText().length(), 6, 8) == false) {
            valid = false;
            JOptionPane.showMessageDialog(null, "Post code must be 6-8 characters long (include the space)");
        }
        
        return valid;
    }
